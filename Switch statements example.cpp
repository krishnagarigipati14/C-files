// USING SWITCH STATEMENTS

#include<stdio.h>

int main(){
	printf("Enter the day: ");
	int day;
	scanf("%d", &day);
	
	switch(day){
		case 1:
			printf("Monday");
			break;
			
		case 2:
			printf("Tuesday");
			break;
			
		case 3:
			printf("Wednesday");
			break;
			
		
	}
	
	return 0;
	
}
